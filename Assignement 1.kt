fun main(){
    var time =-8

    if (time >=1 && time <=10){
        println("GOOD MORNING")

    }else if (time >=11 && time <=13){
        println("NOON")

    }else if(time >=14 && time <=16){
        println("GOOD AFTERNOON")
    }
    else if (time >=17 && time <=20){
        println("GOOD EVENING")

    }else if (time >=21 && time <=24){
        println("GOOD NIGHT")

    }else{
        print("UNRECOGNIZED TIME")
    }
}
